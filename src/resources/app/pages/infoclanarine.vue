    ...mapState({
      appName(state) {
        return this.$_.find(state.themeConfigurations, { key: "appName" }).value;
      },
    }),
  },
  mounted() {
  window.addEventListener('resize', this.handleResize);
  this.handleResize();  // Call once to set the initial width

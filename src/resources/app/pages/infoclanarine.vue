beforeDestroy() {
  window.removeEventListener('resize', this.handleResize);
},


  methods: {
    handleResize() {
    this.windowWidth = window.innerWidth;
    this.updateStyles();
  },
  updateStyles() {
      if (this.windowWidth >= 801) {
        require('../assets/scss/infoclanarine_desktop.scss');
